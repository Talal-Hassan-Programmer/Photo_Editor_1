# Photo Editor

#Imports
from tkinter import Tk, Menu


# Main App
#------------------------------------------------------------------------------------------------------------------------------------------------------------->

App = Tk()


#App configs
#------------------------------------------------------------------------------------------------------------------------------------------------------------->
App.title("Photo Editor") 
App.geometry("800x600")
App.config(bg="white")
App.resizable(False, False)



#Creating a menu bar
#------------------------------------------------------------------------------------------------------------------------------------------------------------->
MenuBar = App.menu = Menu(App)
App.config(menu=MenuBar)






#Running the app
#------------------------------------------------------------------------------------------------------------------------------------------------------------->

if __name__ == "__main__":
    App.mainloop()
